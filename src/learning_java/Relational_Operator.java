package learning_java;

public class Relational_Operator {

	public static void main(String[] args) 
	{
		int num=20;
		int num1=10;
		System.out.println("num > num1 ---" + (num >num1));
		System.out.println("num < num1 ---" + (num < num1));
		System.out.println("num >= num1 ---" + (num >= num1));
		System.out.println("num <= num1 ---" + (num <=num1));
		System.out.println("num == num1 ---" + (num ==num1));
		System.out.println("num != num1 ---" + (num !=num1));
	}

}
