package assingment;

public class Default_Value_Array {

	public static void main(String[] args)
	{
		byte b[]=new byte[3];
		b[0]=34;
		b[1]=11;
		
		System.out.println(b[2]);
		
		short s[]=new short[3];
		s[0]=34;
		s[1]=11;
		
		System.out.println(s[2]);
		
		boolean b1[]=new boolean[3];
		b1[0]=true;
		b1[1]=false;
		
		System.out.println(b1[2]);
		
		
		char c[]=new char[3];
		c[0]='a';
		c[1]='s';
		System.out.println(c[2]);
		
		long l[]=new long[3];
		l[0]=567898765;
		l[1]=23445657;
		System.out.println(l[2]);
		
		float f[]=new float[3];
		f[0]=6.7f;
		f[1]=2.3f;
		System.out.println(f[2]);
		
		double d[]=new double[3];
		d[0]=65456.76546786;
		d[1]=65458765.4356543453;
		System.out.println(d[2]);
		
	}

}
